using namespace std;

class Solution {
    public:
    void solve() {
        int n;
        cin >> n;

        vector<int> v(n);
        for (auto& input : v) {
            cin >> input;
        }

        int curr = v[n-1];
        for(int i = 0; i < n; i++) {
            if(v[i] >= curr) {
                /* Print number*/ {
                    cout<<n-i-1<<endl;
                    return;
                }
            }
        }
    }
};

int32_t main() {

    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int testCases = 1;
    cin >> testCases;

    while (testCases--) {
        Solution obj;
        obj.solve();
    }

    return 0; 
}
